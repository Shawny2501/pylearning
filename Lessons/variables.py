# Integer
int_var = 10
print(int_var)

# Floating-point number
float_var = 10.5
print(float_var)

# Complex number
complex_var = 3 + 4j
print(complex_var)

# String
str_var = "Hello, World!"
print(str_var)

# Boolean
bool_var = True
print(bool_var)


# List
list_var = [1, 2, 3, 4, 5]
print(list_var)

# Tuple
tuple_var = (1, 2, 3, 4, 5)
print(tuple_var)

# Set
set_var = {1, 2, 3, 4, 5}
print(set_var)

# Dictionary
dict_var = {"name": "Alice", "age": 25}
print(dict_var)

# NoneType
none_var = None
print(none_var)
